class LanguageService {
  private currentLanguage: string = 'en';

  constructor() {
    // Load language from localStorage or browser preference
    const savedLanguage = localStorage.getItem('app_language');
    if (savedLanguage && this.isLanguageSupported(savedLanguage)) {
      this.currentLanguage = savedLanguage;
    } else {
      // Try to detect browser language
      const browserLang = navigator.language.split('-')[0];
      if (this.isLanguageSupported(browserLang)) {
        this.currentLanguage = browserLang;
      }
    }
    this.updateDirection();
  }

  getCurrentLanguage(): string {
    return this.currentLanguage;
  }

  setLanguage(languageCode: string): void {
    if (this.isLanguageSupported(languageCode)) {
      this.currentLanguage = languageCode;
      localStorage.setItem('app_language', languageCode);
      this.updateDirection();
      // Dispatch event to notify components about language change
      window.dispatchEvent(new Event('languageChanged'));
    }
  }

  private updateDirection(): void {
    const language = SUPPORTED_LANGUAGES.find(lang => lang.code === this.currentLanguage);
    document.documentElement.dir = language?.rtl ? 'rtl' : 'ltr';
    document.documentElement.lang = this.currentLanguage;
  }

  translate(key: string, params: Record<string, string | number> = {}): string {
    let translation = translations[key]?.[this.currentLanguage] || translations[key]?.['en'] || key;
    
    // Replace placeholders with actual values
    Object.keys(params).forEach(param => {
      translation = translation.replace(`{${param}}`, String(params[param]));
    });
    
    return translation;
  }

  isLanguageSupported(languageCode: string): boolean {
    return SUPPORTED_LANGUAGES.some(lang => lang.code === languageCode);
  }

  getSupportedLanguages(): Language[] {
    return SUPPORTED_LANGUAGES;
  }

  getCurrentLanguageInfo(): Language | undefined {
    return SUPPORTED_LANGUAGES.find(lang => lang.code === this.currentLanguage);
  }
}

const languageService = new LanguageService();
export default languageService;